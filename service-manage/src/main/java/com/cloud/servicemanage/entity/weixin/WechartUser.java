package com.cloud.servicemanage.entity.weixin;

import java.util.Date;

public class WechartUser {
    private String id;//唯一编码
    private String openid;//微信id
    private String userName;//姓名
    private String email;//邮箱地址
    private String phone;//手机号
    private String password;//登录密码
    private String recruitIId;//招聘岗位id
    private Date createTime;//创建时间
    private String creatorId;//创建人id
    private Date modifyTime;//修改时间
    private String modifierId;//修改人
    private Integer isApproved;//0待审核 1审核通过  2审核失败
    private String depId;//部门编号

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getOpenid() {
        return openid;
    }

    public void setOpenid(String openid) {
        this.openid = openid;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getRecruitIId() {
        return recruitIId;
    }

    public void setRecruitIId(String recruitIId) {
        this.recruitIId = recruitIId;
    }

    public Date getCreateTime() {
        return createTime;
    }

    public void setCreateTime(Date createTime) {
        this.createTime = createTime;
    }

    public String getCreatorId() {
        return creatorId;
    }

    public void setCreatorId(String creatorId) {
        this.creatorId = creatorId;
    }

    public Date getModifyTime() {
        return modifyTime;
    }

    public void setModifyTime(Date modifyTime) {
        this.modifyTime = modifyTime;
    }

    public String getModifierId() {
        return modifierId;
    }

    public void setModifierId(String modifierId) {
        this.modifierId = modifierId;
    }

    public Integer getIsApproved() {
        return isApproved;
    }

    public void setIsApproved(Integer isApproved) {
        this.isApproved = isApproved;
    }

    public String getDepId() {
        return depId;
    }

    public void setDepId(String depId) {
        this.depId = depId;
    }
}
